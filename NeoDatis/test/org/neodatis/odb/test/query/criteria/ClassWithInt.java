package org.neodatis.odb.test.query.criteria;

public class ClassWithInt {
	private int i;
	private String name;

	public ClassWithInt(int i, String name) {
		super();
		this.i = i;
		this.name = name;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
